namespace AniNexus;

/// <summary>
/// Defines which enum members to select.
/// </summary>
public enum EEnumMemberSelection : byte
{
    /// <summary>
    /// Select all enum members.
    /// </summary>
    All = 0,

    /// <summary>
    /// Select enum members with distinct values.
    /// </summary>
    Distinct = 1
}


namespace AniNexus.MediaProviders;

/// <summary>
/// The season in which a piece of media was released.
/// </summary>
public enum EMediaSeason
{
    /// <summary>
    /// Months December to February
    /// </summary>
    Winter,

    /// <summary>
    /// Months March to May
    /// </summary>
    Spring,

    /// <summary>
    /// Months June to August
    /// </summary>
    Summer,

    /// <summary>
    /// Months September to November
    /// </summary>
    Fall
}
